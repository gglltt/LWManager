function resolveLang(candidate) {
  return SUPPORTED_LANGS.includes(candidate) ? candidate : "it";
}

function getTranslator(lang) {
  const resolved = resolveLang(lang);
  return (key) => translations[resolved]?.[key] || translations.en?.[key] || translations.it?.[key] || key;
}


function translateTeamType(type, lang) {
  const normalizedType = String(type ?? "").trim();
  if (!normalizedType) return "";

  const resolved = resolveLang(lang);
  return TEAM_TYPE_TRANSLATIONS[resolved]?.[normalizedType]
    || TEAM_TYPE_TRANSLATIONS.it?.[normalizedType]
    || normalizedType;
}

